# ============================================================
# expander 設定
# ============================================================
HELP_EXPANDER_KEY = "batch_processing_help_expander"
HELP_EXPANDER_TITLE = "詳細説明（クリックで展開）"


# ============================================================
# 詳細説明：一括処理
# ============================================================
BATCH_USAGE_TEXT = """

##### 1. このページの役割

- 複数の音声ファイルをまとめて処理し，
  **文字起こし・話者分離・結合・重複箇所検出**を行うページです。

- 最終的な出力は，
  **重複箇所マーカー付きの逐語録準備テキスト**です。

- このページは，完成版の議事録を作るページではありません。
  正式な議事録作成の前段階として，
  話者分離済みの結合テキストを整えるためのページです。

---

##### 2. 全体の処理の流れ

1. 左カラム（サイドバー）で **文字起こしモデル** と **話者分離モデル** を選択します。
（原則，デフォルトのままで良いです．特に変更する必要はありません．）

2. サイドバーで **言語コード** を選択します。
日本語音声の場合は，通常は `ja` を選択します。

3. メインエリアで，処理対象の音声ファイルをアップロードします。

4. 「一括処理」ボタンを押すと，音声ファイルについて，
   文字起こしと話者分離を順番に実行します。

5. 各ファイルの処理結果を，1 本のテキストに結合します。

6. 結合後のテキストに対して，重複している可能性がある箇所を検出し，
   該当箇所にマーカーを付けます。

7. 最終的に，
   **重複箇所マーカー付きの逐語録準備テキスト**をダウンロードできます。

---

##### 3. 出力されるテキストについて

- 出力されるのは，
  **話者分離済みテキストを結合し，重複箇所マーカーを入れたテキスト**です。

- このテキストは，逐語録を作成するための準備テキストとして利用します。

- 重複箇所マーカーは，機械的に検出された候補です。
  実際に削除・修正する箇所は，人手で確認してください。

---

##### 4. 注意点

- 音声ファイルの音質が悪い場合，文字起こし精度が低下することがあります。

- 複数人が同時に話している場合，話者分離が不安定になることがあります。

- 最終テキストは，逐語録作成の前に必ず人手で確認してください。

</div>
"""


