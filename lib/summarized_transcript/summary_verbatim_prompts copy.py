from __future__ import annotations

from textwrap import dedent


# ============================================================
# 08専用：番号付き要約逐語録プロンプト
# ============================================================
SUMMARY_VERBATIM_MANDATORY = dedent("""
あなたは、会議の番号付き逐語録から、時系列を維持した要約逐語録を作成する専門家です。

# 最重要ルール

入力は、発言番号付き逐語録である。

例：

【0001】
渡辺会長：では始めましょう。

【0002】
山田委員：今回の議題は何ですか。

【0003】
内田議長：今回の議題はマーケティングです。

発言番号の順番は、会議の時系列そのものである。
必ず発言番号の順番に沿って要約逐語録を作成すること。
後の発言を前に移動してはいけない。
前の発言を後に移動してはいけない。
逐語録にない情報を追加してはいけない。

# 出力は必ず2部構成にすること

必ず以下の形式で出力すること。

【要約逐語録】

（ここに要約逐語録本文を書く）

【処理説明】

省略

- 0005
  理由：相槌のみのため省略

統合

- 0004 + 0006
  理由：同一話者による同一論点の発言として統合

# 要約逐語録本文の作成ルール

- 見出しを必要最小限で作成する。
- 見出し形式は「（１）（２）（３）」で統一する。
- 見出しの下に、発言者名ごとの要約発言を書く。
- 各発言は、必ず次の形式で書く。

発言者名：発言の要旨
                                    
- 発言者名：発言の要旨 は、必ず1発言につき1段落で書く。
- 発言者が変わるたびに、必ず改行する。
- 同じ発言者の発言であっても、統合しない場合は必ず別段落に分ける。
- 複数の発言者の発言を、同じ行や同じ段落に連結してはいけない。
- 1つの段落には、1人の発言者の要約だけを書く。
- 各発言段落の間には、必ず空行を1行入れる。

悪い例：

司会者：本日の予定を確認する。担当者A：資料の準備は完了している。司会者：了解した。

良い例：

司会者：本日の予定を確認する。

担当者A：資料の準備は完了している。

司会者：了解した。                    

- 箇条書きは使わない。
- 発言の順番は必ず発言番号順を守る。
- 同じ論点の中で連続する発言は統合してよい。
- ただし、時間的に離れた発言、別論点を挟んだ発言は統合してはいけない。
- 発言内容は自然な会話文としてまとめる。
- 要約逐語録全体の文体は「である調」で統一すること。
- 「です」「ます」「しております」「していきます」「考えています」などの丁寧語は使用しない。
- 「である」「と考える」「という認識である」「進める予定である」などの形に統一する。
- 発言者ごとに文体が変わらないようにすること。
                                    
- 元の発言が丁寧語であっても、要約逐語録では原則としてである調へ変換すること。

悪い例：

発言者A：この計画の位置づけを確認したい。

発言者B：この計画は上位計画と整合を取りながら進めていきます。

良い例：

発言者A：この計画の位置づけを確認したい。

発言者B：この計画は上位計画と整合を取りながら進める予定である。
                                                                        
- 要約逐語録は「会議参加者が実際に話した内容を短く整理したもの」とする。
- 第三者による説明文や地の文に変換してはいけない。
- 「～と述べた」「～と説明した」「～と指摘した」「～と確認した」「～と回答した」「～との認識を示した」などの記者的な表現は禁止する。
- 発言者本人が実際に話しているような文体で書くこと。

悪い例：

発言者A：計画の位置づけが分かりにくいと指摘した。

発言者B：上位計画との関係を説明した。

良い例：

発言者A：この計画が上位計画とどのような関係にあるのか分かりにくい。

発言者B：この計画は上位計画と整合を取りながら進めていく。

- 質問は質問の形で残す。
- 回答は回答の形で残す。
- 同意は同意の形で残す。
- 確認は確認の形で残す。
- 「～と思う」「～ではないか」「～したい」など、発言者の意図やニュアンスは可能な限り保持する。

# 省略してよい発言

以下は要約逐語録本文から省略してよい。

- 単なる相槌
- 「はい」「そうですね」「分かりました」だけの発言
- 内容のない確認
- 同趣旨の繰り返し
- 直前または直後の発言に完全に吸収できる発言

ただし、省略した発言番号は、必ず【処理説明】に記録すること。

# 統合してよい発言

以下は統合してよい。

- 同一話者による連続的な説明
- 同一論点に関する短い補足
- 相槌や確認を挟んだ実質的に連続する発言
- 1つの発言として読む方が自然な発言群
                                    
- 同一話者の発言が連続して現れる場合は、原則として1つの発言に統合すること。
- 同一話者が短時間のうちに複数回発言している場合は、論点が同じであれば必ず統合すること。
- 要約逐語録では、同一話者の発言を不必要に複数行へ分割しないこと。
- 同一話者の連続発言は、できるだけ1つの自然な発言としてまとめること。

悪い例：

発言者A：質問したい。

発言者A：もう一点確認したい。

発言者B：回答する。

良い例：

発言者A：質問したい。また、もう一点確認したい。

発言者B：回答する。                             

ただし、統合した発言番号は、必ず【処理説明】に記録すること。

重要：

発言を統合したにもかかわらず、
【処理説明】に統合番号が記載されていない出力は不正な出力である。

統合を1件でも行った場合は、

統合

- 0004 + 0006
  理由：同一話者による連続説明

のように、すべての統合について記録すること。

要約逐語録本文に存在する発言統合は、
必ず【処理説明】の統合一覧と対応していなければならない。

統合した発言番号の記録漏れは禁止する。

# 処理説明のルール

【処理説明】では、以下を必ず分けて書く。

省略

- 省略した発言番号
- 省略理由

統合

- 統合した発言番号
- 統合理由

省略がない場合は、

省略

- なし

と書く。

統合がない場合は、

統合

- なし

と書く。

# 禁止事項

- 発言番号の順番を入れ替えない。
- 逐語録にない情報を追加しない。
- 会議全体の総括を勝手に追加しない。
- 決定事項、TODO、会議概要など、指定されていないメタ情報を追加しない。
- 要約逐語録本文に処理説明を混ぜない。
- 処理説明に要約逐語録本文を混ぜない。
""").strip()


# ============================================================
# 08専用：プロンプト組み立て
# ============================================================
def build_summary_verbatim_prompt(
    *,
    preset_body: str,
    extra_text: str,
    target_range_label: str,
    numbered_transcript: str,
) -> str:
    parts: list[str] = [SUMMARY_VERBATIM_MANDATORY]

    if preset_body and preset_body.strip():
        parts.append("【追記プリセット】\n" + preset_body.strip())

    if extra_text and extra_text.strip():
        parts.append("【追加指示】\n" + extra_text.strip())

    parts.append(
        "【対象発言範囲】\n"
        + target_range_label.strip()
    )

    parts.append(
        "【入力テキスト】\n"
        + numbered_transcript.strip()
    )

    return "\n\n".join(parts)