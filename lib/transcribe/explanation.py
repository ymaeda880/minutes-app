FAQ_TEXT = """
<div style="line-height:1.7">

## 1. よくある質問（FAQ）

### Q1. 通常はどの入力方式を使えばよいですか？

通常は「既存ジョブ」を使います。

前ページの音声分割で作成された split/ の音声チャンクを，
そのまま読み込むことができます。

---

### Q2. 英語音声の場合はどうすればよいですか？

サイドバーの「言語コード」を `en` に変更してください。

日本語音声の場合は `ja` のままで構いません。

---

### Q3. サイドバーの設定は変更する必要がありますか？

通常は変更する必要はありません。

ただし，処理対象の音声ファイルを一部だけにしたい場合は，
「処理する音声ファイル」で対象を選び直してください。

---

### Q4. 文字起こし結果はどこに保存されますか？

対象ジョブの `transcript/` フォルダに個別結果が保存されます。

全体を結合したテキストは `transcript_combined/` に保存されます。

---

### Q5. 連結テキストをダウンロードしないと次の処理に進めませんか？

いいえ。

後続処理は，サーバー内部ストレージに保存された文字起こし結果から行えます。

パソコンに保存したい場合だけ，連結テキストをダウンロードしてください。

---

### Q6. AI料金は必ず表示されますか？

必ず表示されるとは限りません。

AI結果として cost が返ってきた場合だけ，料金を表示します。

取得できない場合は，推計せずに空欄または注記として扱います。

</div>
"""


