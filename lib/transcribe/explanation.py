# ============================================================
TRANSCRIBE_USAGE_TEXT = """
<div style="line-height:1.7">

## 1. 使い方

1. 音声ファイルを読み込みます。

   通常は，「音声ファイル分割」によりサーバー内部に保存されたファイルを使用します。

   「音声ファイルの設定」で「既存ジョブ」を選択し，
   「ジョブ選択」から「ジョブ（分割された音声ファイルの集合体）」を選択します。

2. 「文字起こしを実行 + ストレージ保存」のボタンを押すと，
   選択された音声ファイルの集合体に対して，順番に文字起こしが行われます。

3. すべての音声ファイルの文字起こしが終わると，
   それぞれの文字起こし結果が結合されます。

4. 文字起こしがすべて終わると，
   「ストレージに保存しました」というメッセージが表示されます。

5. この時点で，各ファイルごとの文字起こし結果と，
   すべてのファイルを結合した連結テキストが，
   サーバー内部ストレージに保存されています。

6. 「連結テキストをパソコンにダウンロード」ボタンを押すと，
   文字起こししたテキストファイルを1つに連結したものを，
   自分のパソコンにダウンロードできます。

---

## 2. このページの役割

このページは，AIを使って音声ファイルを文字起こしするページです。

主に，前ページの「音声ファイル分割」で作成された分割済み音声ファイルを読み込み，
1ファイルずつ順番に文字起こしします。

複数の音声ファイルを処理した場合は，
最後にすべての文字起こし結果を1つのテキストとして連結します。

連結テキストは，次の処理である話者分離・整形や議事録作成に引き継ぐことができます。

---

## 3. 全体の処理の流れ

1. ユーザーごとの Storages フォルダを確認します。
2. 入力方式を選択します。
3. 既存ジョブ，または新規アップロード音声を選択します。
4. サイドバーで処理対象の音声ファイルを確認します。
5. サイドバーでモデル，返却形式，言語コード，プロンプトを確認します。
6. 「文字起こしを実行 + ストレージ保存」ボタンを押します。
7. 各音声ファイルについて AI 文字起こしを実行します。
8. ファイルごとの文字起こし結果を画面に表示します。
9. ファイルごとの文字起こし結果を transcript フォルダに保存します。
10. 最後に，すべての文字起こし結果を連結します。
11. 連結テキストを transcript_combined フォルダに保存します。
12. 画面上で料金，処理時間，保存先を確認できます。

---

## 4. 入力方式

### 4-1. 既存ジョブ

通常はこちらを使います。

前ページの「音声ファイル分割」で作成されたジョブを選択し，
そのジョブの split/ フォルダに保存されている分割済み音声ファイルを読み込みます。

この方式では，音声ファイルを再アップロードする必要はありません。

---

### 4-2. 新規アップロード

音声ファイルをこのページで直接アップロードする方式です。

複数ファイルをアップロードすると，
アップロード順に番号を付けて，新しいジョブの split/ フォルダへ保存します。

その後，保存された音声ファイルを対象として文字起こしを実行します。

---

## 5. ジョブとは

このアプリでは，音声ファイル分割から文字起こし，議事録作成までの一連の作業単位を
「ジョブ」と呼びます。

ここでのジョブは，主に次の内容を含みます。

- 分割済み音声ファイル
- 文字起こし結果
- 連結テキスト
- 処理ログ
- job.json

---

## 6. 出力内容

文字起こしを実行すると，次の内容が作成されます。

### 6-1. ファイル別の文字起こし結果

各音声ファイルごとに，次の内容を表示・保存します。

- 個別の文字起こしテキスト
- 処理時間
- 音声長
- request-id
- 保存先
- cost が取得できた場合の料金情報

---

### 6-2. 連結テキスト

すべての音声ファイルを処理した後に，
全ファイルの文字起こし結果を1つに結合します。

各ファイルの境界には，次のようなつなぎ目マーカーが自動挿入されます。

<pre>
----- ここがつなぎ目です（ファイル名 と次のファイルの間）-----
</pre>

この連結テキストは，後続の話者分離・整形や議事録作成に使う想定です。

---

## 7. AI利用について

このページでは AI を使用します。

AI実行は common_lib.ai.routing.transcribe_audio を通じて行われます。

ページ側で API を直接呼び出したり，料金を推計したりしません。

cost/usage は，AI結果として返ってきた範囲だけを表示・記録します。

</div>
"""


