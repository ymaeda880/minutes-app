STORAGE_STRUCTURE_TEXT = """
<div style="line-height:1.7">

## 1. 保存先の基本構造

文字起こし結果は，対象ジョブのフォルダ内に保存されます。

基本構造は次の通りです。

<pre>
Storages/&lt;user&gt;/minutes_app/&lt;YYYY-MM-DD&gt;/&lt;job_id&gt;/
</pre>

主なフォルダは次の通りです。

<pre>
split/
transcript/
transcript_marked/
transcript_combined/
logs/
job.json
</pre>

---

## 2. split フォルダ

`split` フォルダには，文字起こし対象の音声ファイルが保存されています。

前ページの音声分割で作成された分割音声や，
このページで新規アップロードした音声ファイルがここに置かれます。

---

## 3. transcript フォルダ

`transcript` フォルダには，音声ファイルごとの個別文字起こし結果が保存されます。

ファイル名には処理順と元の音声ファイル名が含まれます。

---

## 4. transcript_combined フォルダ

`transcript_combined` フォルダには，
全音声ファイルを順番につないだ連結テキストが保存されます。

後続の話者分離・整形や議事録作成では，
この連結テキストを利用する想定です。

---

## 5. transcript_marked フォルダ

`transcript_marked` フォルダは，
後続の話者分離・整形後のテキストを保存するためのフォルダです。

このページでは主に作成・保持されるだけで，
中身は後続ページで利用されます。

---

## 6. logs フォルダ

`logs` フォルダには，`process.log` が保存されます。

文字起こし開始，対象ファイル，保存先，処理完了などの記録が残ります。

---

## 7. job.json

`job.json` には，ジョブID，ユーザー，保存パス，処理ステータスなどが保存されます。

後続ページが同じジョブを参照するための基礎情報になります。

</div>
"""


