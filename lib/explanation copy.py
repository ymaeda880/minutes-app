def render_speaker_prep_expander() -> None:
    """
    「③ 話者分離・整形」ページのロジックと使い方を expander で描画します。
    pages/xx_話者分離.py などから呼び出して使います。
    """
    with st.expander(
        "ℹ️ ③ 話者分離・整形（議事録前処理）のしくみと使い方（クリックで展開）",
        expanded=False,
    ):
        st.markdown(
            """
## 1. このページの役割（議事録前処理）

- このページは、文字起こし（.txt）を **話者ごとに分離・整形** するための前処理ページです。
- 1つの巨大テキストを一発で整形するのではなく、**複数の transcript（複数 .txt）を順番に処理して保存**し、最後に **つなぎ行を挟んで連結**します。
- 出力は、次工程（議事録作成・重複検出など）の入力として利用できます。

---

## 2. 入力方式（2通り）

このページは、入力を次の2通りから選べます。

### A) 既存ジョブ（storage より読み込み）
- すでに Storages に存在する job を選びます。
- 選択した job の `transcript/*.txt` を読み込み、順番に話者分離します。

### B) 新規アップロード（drop → 新規job作成 → transcript/ に保存）
- 文字起こしテキスト（.txt）を **複数** アップロードできます。
- アップロード順が、そのまま **処理順** になります。
- **重要：アップロードしただけでは job は作りません。**  
  「📦 アップロードを transcript/ として保存して新規ジョブを作成」ボタンを押した時点で、Storages に新規 job を作成し、`transcript/` に保存します（確定）。

---

## 3. 連続処理（バッチ）の流れ

1. 入力を確定します（既存 job を選択、または新規アップロード→ジョブ作成）。
2. `transcript/*.txt` を順番に処理し、話者分離結果を保存します：
   - `transcript_speaker_separated/`（ファイルごとに保存）
3. それらを「つなぎ行」を挟んで連結し、まとめて保存します：
   - `transcript_speaker_separated_combined/`

> 「つなぎ行」は、どのファイルとどのファイルの間かが分かるように挿入されます。

---

## 4. drop を “連続して” 使える（繰り返し運用）

- 新規アップロード（drop）は **何回でも繰り返し** 行えます。
- たとえば、  
  - 午前の会議 → drop → job作成 → 話者分離  
  - 午後の会議 → drop → job作成 → 話者分離  
  のように、別のアップロードを **別ジョブとして連続運用** できます。
- 同じアップロード内容で画面が再実行（rerun）されても、作成済み job があれば復元して続行できます。

---

## 5. プロンプト構成（lib.prompts）

- `SPEAKER_PREP` を `get_group()` で取得し、プロンプト UI を組み立てます。
- 実行時は `build_prompt()` により、次の 3パートを連結して 1本のプロンプトにします。

1. **必須プロンプト（mandatory）**
2. **プリセット本文（preset）**
3. **追加指示（extra）**

---

## 6. 実行ロジック（バッチ実行）

「② transcript を順番に話者分離（ストレージ保存＋連結）」を押すと：

1. 確定済みの job_dir と transcript_files を取得します。
2. transcript を先頭から順に LLM に投げ、結果を保存します。
3. 途中経過（進捗）を表示しながら最後まで処理します。
4. 最後に連結結果を保存し、ダウンロードも可能です。

※ 基本は 1ファイルずつ順番に処理します（ページ側でリトライは行いません）。

---

## 7. 保存先（ストレージ）

選択された job フォルダ配下に保存されます：

- 入力：`transcript/`
- 個別結果：`transcript_speaker_separated/`
- 連結結果：`transcript_speaker_separated_combined/`
- ログ：`logs/process.log`

---

## 8. モデル・料金の目安

- サイドバーでモデルを選択できます（OpenAI / Gemini）。
- 表示されるトークン数・料金は概算です（USD/JPY は換算用）。
- 長文ほど時間とコストが増えるため、必要に応じて「軽量」「超軽量」プロンプトも使ってください。
"""
        )


