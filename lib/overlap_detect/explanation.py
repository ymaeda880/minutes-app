# 詳細説明：検出ロジック
# ============================================================
DETECTION_LOGIC_TEXT = """
<div style="line-height:1.7">

## 1. 検出ロジックの概要

このページでは，つなぎ目マーカーでテキストを複数のセグメントに分けます。

そして，各つなぎ目について，

- 前半セグメントの末尾
- 後半セグメントの先頭

を比較します。

後半セグメントの先頭にある数文を抜き出し，
それに近い文字列が前半セグメントの末尾にあるかを探します。

---

## 2. 比較対象

前半側は，末尾の一定文字数だけを比較対象にします。

現在の設定では，前半側の末尾から次の文字数を対象にします。

<pre>
OVERLAP_CHARS = 700
</pre>

後半側は，先頭から一定文字数を取り出します。

<pre>
HEAD_CHARS = 400
HEAD_SENTENCES = 3
</pre>

---

## 3. 正規化

照合時は，空白，改行，句読点などを除去して正規化した文字列で比較します。

主な正規化は次の通りです。

- 全角スペースを削除
- 改行を削除
- 句読点を削除
- 半角スペースを削除

これにより，句読点や改行の違いに左右されにくくします。

---

## 4. 話者ラベル除去

後半セグメントの先頭に，次のような話者ラベルが付いている場合，
比較前にそれを除去します。

- S1:
- S2:
- [S1]&#58; - [s12]&#58; - 司会:
- MC:

これにより，話者ラベルの違いによる検出失敗を減らします。

---

## 5. head shift

後半側の先頭が少しだけズレている場合に備えて，
先頭文字を少しずつ削った候補も比較します。

現在の設定では，最大で次の回数だけ候補をずらします。

<pre>
HEAD_SHIFT_TRIES = 3
</pre>

先頭が欠けていても拾えるようにするための処理です。

---

## 6. BEGIN_TAG の挿入

重複開始位置が見つかった場合，
前半セグメント側の該当位置に次のタグを挿入します。

<pre>
-----ここから重複-----
</pre>

このタグ以降を人が確認し，
必要に応じて重複部分を削除・修正します。

---

## 7. 検出の目的

この処理の目的は，重複の完全削除ではありません。

編集の起点となる開始位置をマーキングすることが目的です。

</div>
"""


# ============================================================
# 詳細説明：パラメータ
# ============================================================
PARAMETER_TEXT = """
<div style="line-height:1.7">

## 1. 一致とみなす最低文字数

サイドバーの「一致とみなす最低文字数」は，
重複として扱うために必要な最小一致文字数です。

既定値は次の通りです。

<pre>
DEFAULT_MIN_MATCH_SIZE = 20
</pre>

値を小さくすると，短い一致も拾いやすくなります。

ただし，誤検出が増える可能性があります。

値を大きくすると，より長い一致だけを検出します。

ただし，短い重複を見逃す可能性があります。

迷ったらデフォルト値から始め，結果を見て調整してください。

---

## 2. autojunk

autojunk は，Python の SequenceMatcher による自動ジャンク判定です。

通常は ON のままで構いません。

- ON：一般的に安定・高速です。
- OFF：短い一致も拾いやすくなる一方，少し遅くなることがあります。

短い重複が多くて見逃す場合は，OFF を試してください。

---

## 3. BEGIN_TAG

検出された重複開始位置には，次のタグを挿入します。

<pre>
-----ここから重複-----
</pre>

このタグは，後から人が重複箇所を確認するための目印です。

</div>
"""


