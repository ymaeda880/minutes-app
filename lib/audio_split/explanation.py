STORAGE_STRUCTURE_TEXT = """

##### 1. 保存先の基本構造

分割実行時には、ユーザーごとの Storages 配下にジョブ単位で保存されます。
保存先は概ね次の構造です。

<pre>
Storages/&lt;user&gt;/minutes_app/&lt;YYYY-MM-DD&gt;/&lt;job_id&gt;/
</pre>

各ジョブフォルダの中には、次のフォルダとファイルが作成されます。

<pre>
original/
split/
transcript/
logs/
job.json
</pre>

---

##### 2. original フォルダ

`original` フォルダには、アップロードされた元の音声ファイルが保存されます。<br>
後から再処理する場合や、元音声を確認する場合に使用します。



##### 3. split フォルダ

`split` フォルダには、分割済み音声ファイルが保存されます。<br>
また、分割済み音声をまとめた ZIP ファイルもこのフォルダに保存されます。



##### 4. transcript フォルダ

`transcript` フォルダは、後続の文字起こし結果を保存するためのフォルダです。<br>
このページでは文字起こしは行わないため、通常は空の状態で作成されます。



##### 5. logs フォルダ

`logs` フォルダには、`process.log` が保存されます。<br>
処理開始、保存パス、分割条件、ZIP作成完了などの記録が残ります。



##### 6. job.json

`job.json` には、次のような情報が保存されます。

- job_id
- user
- 作成日時
- 保存パス
- 分割条件
- 処理ステータス
- 分割インデックス
- 音声全体の長さ

このファイルにより、後続ページで同じジョブを参照しやすくなります。

</div>
"""


