AUDIO_SPLIT_USAGE_TEXT = """

##### 1. 使用法

1. 音声ファイルをアップロードします。  
   「Drag and drop」にファイルをドラッグするか、  
   「Browse files」からパソコン上のファイルを読み込んでください。

2. ファイルが読み込まれると、自動的に音声ファイルの解析が行われます。

3. 音声分割の準備が終わると、  
   「分割済み音声を生成（ZIP作成＋ストレージ保存）」ボタンが表示されます。  
   このボタンを押すと、ファイル分割の処理が行われます。

4. 処理が終わると、  
   「分割ファイルをストレージに保存しました」というメッセージが表示されます。  
   この時点で、サーバーの内部ストレージに分割ファイルが保存されています。

5. 分割したファイルを自分のパソコンに保存したい場合は、  
   「分割済み音声をZIPでダウンロード」ボタンを押してください。  
   パソコンに保存する必要がない場合は、このボタンを押す必要はありません。  
   以後の処理は、サーバーの内部ストレージに保存したファイルから行うことができます。

---

##### 2. このツールが行う処理（全体の流れ）

1. アップロードした MP3 / WAV / MP4 を読み込みます。
2. 指定したチャンク長（分）で音声を分割します。
3. 隣接チャンクには、指定したオーバーラップ（重なり）を付けます。
4. 各チャンクには開始時刻・終了時刻・持続時間を付与します。
5. 指定フォーマット（MP3 / WAV）でファイルを書き出し、ZIP にまとめます。
6. 分割一覧（プレビュー表）と ZIP ダウンロードボタンを表示します。
7. 元ファイル、分割ファイル、ZIP、ログ、job.json をサーバー内部のストレージ（Storages）に保存します。

<span style="color:red; font-size:14px;">
左側（サイドバー）の設定は、基本的にデフォルトのままで構いません。
</span>

---

##### 3. 出力されるファイル

Storages には、主に次のファイル・フォルダが保存されます。

- 元の音声ファイル
- 分割済み音声ファイル
- 分割済み音声をまとめた ZIP ファイル
- 分割インデックス
- 処理ログ
- job.json

ZIP の中には、次のような分割音声ファイルが格納されます。

- `audio_part000_000000-001500.mp3`
- `audio_part001_001400-002900.mp3`
- `audio_part002_002800-004300.mp3`

ファイル名には、part番号と開始時刻・終了時刻が含まれます。


</div>
"""


# ============================================================
# 詳細説明：パラメータ
# ============================================================
PARAMETER_TEXT = """

##### 1. パラメータの意味

###### 1-1. チャンク長（分）

1チャンクあたりの再生時間です。<br>
たとえば、チャンク長を15分にすると、  
音声はおおよそ次のように切り分けられます。

- 0分〜15分
- 14分〜29分
- 28分〜43分

この例では、オーバーラップが1分あるため、隣り合うチャンクが1分ずつ重なっています。

---

###### 1-2. オーバーラップ（分）

オーバーラップとは、隣り合うチャンクの重複部分です。
文字起こしや話者分離で文脈を保つために有効です。<br>
たとえば、チャンク長15分、オーバーラップ1分の場合、
前のチャンクの最後1分と、次のチャンクの最初1分が重なります。

<span style="color:red; font-size:12px">
オーバーラップは、チャンク長より短い必要があります。
</span>

---

###### 1-3. 書き出しフォーマット（MP3 / WAV）

分割後の音声ファイル形式を指定します。

- MP3：ファイルサイズを抑えやすい形式です。
- WAV：音質を維持しやすい形式です。

<b>通常は、処理を軽くするために MP3 を使用してください。</b>

---

###### 1-4. ビットレート（MP3のみ）

MP3 の品質設定です。<br>
128k〜320k の範囲で指定できます。
ビットレートを高くすると音質は良くなりますが、ファイルサイズは大きくなります。
会議音声の文字起こし前処理であれば、128k〜192k 程度で十分な場合が多いです。

---

###### 1-5. フェード（ms）

クリックノイズ低減のため、各チャンクの先頭と末尾に  
フェードイン／フェードアウトを適用できます。<br>
通常は 0 ms のままで問題ありません。

---

###### 1-6. 最後の「短すぎる尻尾」の吸収

最後の端数部分がオーバーラップより小さい場合、
無理に独立チャンクにせず、前のチャンクに吸収します。
これにより、不要な細切れファイルを防ぐことができます。

</div>
"""


