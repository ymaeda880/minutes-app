FAQ_TEXT = """

##### よくある質問（FAQ）

###### Q1. 文字起こし用に最適な設定は？

一般的には、次の設定が扱いやすいです。

- チャンク長：10〜20分
- オーバーラップ：30秒〜1分
- 出力形式：MP3

Whisper等の文字起こし処理と相性が良い設定です。

---

###### Q2. MP3 と WAV のどちらが良いですか？

WAV は音質を維持しやすい形式です。  
ただし、ファイルサイズが大きくなります。<br>
MP3 はファイルサイズを抑えやすく、共有や保存がしやすい形式です。<br>
<span style="color:red; font-size:14px">
通常は処理を軽くするために MP3 を使用してください。
</span>

---

###### Q3. オーバーラップを付ける理由は何ですか？

分割位置で発言が途中で切れても、前後の文脈を保ちやすくするためです。
AI文字起こしが途中で発話を切り捨てる問題への対策にもなります。

---

###### Q4. ZIP ダウンロード時に警告が出ることがありますか？

LAN 内，自己署名 HTTPS 環境， HTTP 環境では，Chrome が ZIP ダウンロードを
「安全でない」と警告する場合があります。<br>
その場合は、右上に出る警告から「保存」をクリックしてください。
内容はローカルで作成された ZIP ファイルです。

---

###### Q5. ZIP をダウンロードしないと次の処理に進めませんか？

いいえ。
ZIP はパソコンへ保存したい場合のためのものです。
後続処理は、サーバー内部ストレージに保存された分割済み音声ファイルから行うことができます。

</div>
"""


